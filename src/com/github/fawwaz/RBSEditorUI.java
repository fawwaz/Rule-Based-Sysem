    private void readFile(String filename,JTextArea area){
        try{
            FileReader reader = new FileReader(filename);
            area.read(reader, null);
            reader.close();
        }catch(IOException e){
            System.out.println("ERROR");
            e.printStackTrace();
        }
    }
    
